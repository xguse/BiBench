import unittest

import bibench.all as bb

class TestIsa(unittest.TestCase):

    def test_isa(self):
        data, expected = bb.make_isa_data()
        nclusts = 3
        found = bb.isa(data, no_seeds=30)
        self.assertTrue(len(found) == nclusts)


if __name__ == "__main__":
    unittest.main()



